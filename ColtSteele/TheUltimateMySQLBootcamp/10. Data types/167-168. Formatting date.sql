-- / / / / / / / / / / / / / / / / / / / / / / / / / DATE_FORMAT() / / / / / / / / / / / / / / / / / / / / / / / / / / /
-- %d = day without suffix
-- %D = day with suffiix
-- %m = month number
-- %M = month name
-- %y = year in two digits
-- %Y = year in four digits
-- %w = day of the week (monday as 1st day)
-- %W = name of the day (Monday - Sunday)
SELECT birthdt, DATE_FORMAT(birthdt, '%D %M %Y') FROM people;
-- +---------------------+---------------------------------+
-- | birthdt             | DATE_FORMAT(birthdt,'%D %M %Y') |
-- +---------------------+---------------------------------+
-- | 1983-11-11 10:07:35 | 11th November 1983              |
-- | 1943-12-25 04:10:42 | 25th December 1943              |
-- | 2022-09-01 20:58:03 | 1st September 2022              |
-- +---------------------+---------------------------------+


