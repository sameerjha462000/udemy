-- CURDATE() shows the current date
-- CURTIME() shows the current time
-- NOW() shows the current datetime

SELECT CURDATE();
-- +------------+
-- | CURDATE()  |
-- +------------+
-- | 2022-09-01 |
-- +------------+

SELECT CURTIME();
-- +-----------+
-- | CURTIME() |
-- +-----------+
-- | 20:55:50  |
-- +-----------+

SELECT NOW();
-- +---------------------+
-- | NOW()               |
-- +---------------------+
-- | 2022-09-01 20:56:47 |
-- +---------------------+