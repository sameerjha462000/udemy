-- Till Now we have seen data in a table in an independent form
-- But the real world data is messy.

-- In real life, data is interrelated. So that means that there could be multiple tables in a database
-- and also of these could be related to each other.


-- For example : We can have a book_shop database and multiple tables within it.
	-- books
	-- reviews
	-- customers
	-- versions
	-- authors
	-- orders

-- However, the relationship between the tables can be of basically three types
	
	-- One to One relationship (customers and reviews)
	-- One to Many relationship (books and reviews)
	-- Many to Many relationship (authors and books)












	