/**
 * ESCAPE CHARACTERS
 */

console.log('he said I ain\'t happy'); // he said I ain't happy
console.log("he said I ain\"t happy"); // he said I ain"t happy
console.log("he said \"I ain\'t happy\""); // he said "I ain't happy"


/**
 * We also have some special escape characters
 * \n -- new line
 * \t -- tab 
 * \\ -- backslash
 */

console.log("Hello\nworld"); // "Hello
                             //  world"
console.log("Hello\tworld"); // "Hello  world"
console.log("Hello\\world"); // "Hello\world"
