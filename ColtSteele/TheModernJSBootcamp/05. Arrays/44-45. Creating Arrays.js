/**
 * Arrays can be created in two ways 
 */

// To make an empty array
let students = [];

// An array of strings
let colors = ['red', 'orange', 'yellow'];

// An array of numbers
let lottoNums = [19, 22, 56, 12, 51];


// A mixed  array
let stuff = [true, 68, 'cat', null];

/**
 * We can also create an array using Array() constructor
 * But I dont know why would one use that
 */

let nums = new Array();


/**
 * Arrays are indexed
 */

colors[0]; // 'red'
colors[1]; // 'orange'
colors[2]; // 'yellow'
colors[3]; // undefined

/**
 * Just like strings, arrays also have length
 */

colors.length; // 3
lottoNums.length; // 5
stuff.length; // 4




