/**
 * We can also have methods in objects
*/

/**
 * The objects are just like containers. We can have properties and methods in them and group them together so that we can carry them around.
*/

const math = {
    // When we add a function in an object, We call it a method.
    add         : function(x, y){
        return x + y;
    },
    subtract    : function(x, y){
        return x - y;
    },
    multiply    : function(x, y){
        return x * y;
    },
    divide      : function(x, y){
        return x / y;
    }
}




