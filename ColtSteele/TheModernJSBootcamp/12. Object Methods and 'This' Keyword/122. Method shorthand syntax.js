/**
 * We also have method shorthand syntax.
 */

const math = {
    // We do not need to write function keyword in them.
    
    PI : 3.14159265358979323846,
    add(x, y){
        return x + y;
    },
    subtract(x, y){
        return x - y;
    },
    multiply(x, y){
        return x * y;
    },
    divide(x, y){
        return x / y;
    }
}

console.log(math.PI);
console.log(math.add(3, 4));



